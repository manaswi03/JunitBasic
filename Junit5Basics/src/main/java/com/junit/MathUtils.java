package com.junit;

public class MathUtils {
	
	public int add(int a,int b) {
		
		return a + b;
	}

	public int subtrcat(int a,int b) {
		return a - b;
	}
	
	public int multiply(int a ,int b) {
		return a * b;
	}
	
	public int divide(int a ,int b) {
		return a / b ;
	}
	
	
	public Double computeCircleArea(int r) {
		return Math.PI * r * r;
	}
}
